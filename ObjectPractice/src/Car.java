
public class Car
	{

		String plate;
		int year;
		
		public Car(String p, int y)
		{
			plate = p;
			year = y;
		}

		public String getPlate()
			{
				return plate;
			}

		public void setPlate(String plate)
			{
				this.plate = plate;
			}

		public int getYear()
			{
				return year;
			}

		public void setYear(int year)
			{
				this.year = year;
			}

	}
